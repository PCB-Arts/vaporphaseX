//=================================
// included dependencies
#include "utilities/powi.h"

//=================================
// definitions

int powi(int x, unsigned int y) {
	int z = 1;

	while (y) {
		if (y & 1)
			z *= x;

		y >>= 1;
		x *= x;
	}

	return z;
}
