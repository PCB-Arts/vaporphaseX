/**
  * @brief This function handles USART3 global interrupt / USART3 wake-up interrupt through EXTI line 28.
  */
void USART3_IRQHandler(void)
{
  /* USER CODE BEGIN USART3_IRQn 0 */

  //receiver not empty interrupt
  if (READ_BIT(USART3->CR1, USART_CR1_RXNEIE) &&
        READ_BIT(USART3->ISR, USART_ISR_RXNE)) {
	comm_usart_rxne_isr();
  }

  //transmit empty interrupt
  if (READ_BIT(USART3->CR1, USART_CR1_TXEIE) &&
        READ_BIT(USART3->ISR, USART_ISR_TXE)) {
    comm_usart_txe_isr();
  }

  //transmission complete interrupt
  if (READ_BIT(USART3->CR1, USART_CR1_TCIE) &&
        READ_BIT(USART3->ISR, USART_ISR_TC)) {
    comm_usart_tc_isr();
  }

  //Clear all interrupts reason to ignore them
  USART3->ICR = 0xFFFFFFFF;

  /* USER CODE END USART3_IRQn 0 */
  /* USER CODE BEGIN USART3_IRQn 1 */

  /* USER CODE END USART3_IRQn 1 */
}

